export function getPlatformName(name: string) {
  switch (name) {
    case 'openai': {
      return 'OpenAI'
    }
    case 'openrouter': {
      return 'Open Router'
    }
    case 'custom': {
      return 'Custom'
    }
    case 'anthropic': {
      return 'Anthropic'
    }
    case 'pfn': {
      return 'Preferred Networks'
    }
    default: {
      return name
    }
  }
}
